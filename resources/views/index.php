    // Inisialisasi peta Jakarta
    var map = L.map('map').setView([-6.181982088625899, 106.8257607438333], 11);

    // Tambahkan tile layer OpenStreetMap
    L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
        attribution: '&copy; OpenStreetMap contributors',
        maxZoom: 18
    }).addTo(map);

    // Daftar file GeoJSON dan warna masing-masing
    const geojsonLayers = [
        { file: 'data/prod_kab_202413101.geojson', color: '#FF5733' },
        { file: 'data/prod_kab_202413171.geojson', color: '#33FF57' },
        { file: 'data/prod_kab_202413172.geojson', color: '#3357FF' },
        { file: 'data/prod_kab_202413173.geojson', color: '#FF33A1' },
        { file: 'data/prod_kab_202413174.geojson', color: '#8E44AD' },
		{ file: 'data/prod_kab_202413175.geojson', color: '#FFD653' }
    ];

    // Fungsi gaya untuk tiap file berdasarkan warna
    function getStyle(color) {
        return function(feature) {
            return {
                fillColor: color,
                weight: 1,
                opacity: 1,
                color: 'white',
                fillOpacity: 0.7
            };
        }
    }

    // Loop tiap file GeoJSON
    geojsonLayers.forEach(layer => {
        fetch(layer.file)
            .then(response => response.json())
            .then(data => {
                L.geoJSON(data, {
                    style: getStyle(layer.color),
                    onEachFeature: function (feature, geoLayer) {
                        console.log(feature.properties);
                         const namaKab = feature.properties.nmkab || 'Wilayah';
                         const jumlahUsaha = usahaData[namaKab] || 'Data tidak tersedia';
                        const info = `
                                <strong>${namaKab}</strong><br/>
                                Jumlah Usaha: ${jumlahUsaha} usaha
                        `;
                        geoLayer.bindPopup(info);
                    }
                }).addTo(map);
            })
            .catch(err => console.error(`Gagal memuat ${layer.file}:`, err));
    });
</script>


